package com.alxelr.Model;

public class Ticker {
    private String vol;
    private String low;
    private String open;
    private String high;
    private String last;
    private String buy;
    private String buy_amount;
    private String sell;
    private String sell_amount;

    public String getVol() {
        return vol;
    }

    public String getLow() {
        return low;
    }

    public String getOpen() {
        return open;
    }

    public String getHigh() {
        return high;
    }

    public String getLast() {
        return last;
    }

    public String getBuy() {
        return buy;
    }

    public String getBuy_amount() {
        return buy_amount;
    }

    public String getSell() {
        return sell;
    }

    public String getSell_amount() {
        return sell_amount;
    }
}
